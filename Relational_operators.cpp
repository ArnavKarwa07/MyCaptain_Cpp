#include <iostream>
using namespace std;
//Relational operators

int main() {
    int num1 = 10, num2 = 20;

    cout << "Num1: " << num1 << ", Num2: " << num2 << endl;
    cout << "Equal: " << (num1 == num2) << endl;
    cout << "Not Equal: " << (num1 != num2) << endl;
    cout << "Greater Than: " << (num1 > num2) << endl;
    cout << "Less Than: " << (num1 < num2) << endl;
    cout << "Greater Than or Equal To: " << (num1 >= num2) << endl;
    cout << "Less Than or Equal To: " << (num1 <= num2) << endl;

    return 0;
}